          <h2>一些常见的误解</h2>
          <h3>整体追踪和局部追踪：</h3>
          <p>
            整体追踪（定位追踪）和局部追踪（手指/
            手部追踪）是不同的概念。在文章中当我谈到手套时，我主要关注的是它们的局部追踪能力。手套的定位追踪可以通过附加现有的追踪技术（如Vive追踪器、OptiTrack
            标记等）来实现，所以我在上面的讨论中省略了这一部分。
          </p>
          <h3>手指追踪和手部追踪：</h3>
          <p>
            尽管许多硬件都多少加入了手部感知交互功能，但他们实现的程度并不相同。这里比较的主要标准是“手指弯曲的连续性”、“自由度”、“可靠性”和“整体体验”几个方面，而不仅仅是被多数人错误使用的“精度”。如果人们评价某个“精确但不可靠”的解决方案要比某个”可靠且具有更高的自由度但不那么精确”
            的解决方案更好，这是不公平的，我们的评判标准不应该只有精度。
          </p>
          <p>
            在VR中，手指能够连续动作并以任意角度弯曲，与手指只能开关式地做到展开或弯曲两种状态是不同的。这种差异是由不同的追踪方法造成的，Touch和Knuckles都使用了接近传感器对手指进行两种状态的检测，因此它们只能检测手指的“按压”和“松开”两个状态，并通过动画对图形进行补偿，只能捕捉3或5自由度的手指动作。而数据手套和基于视觉的解决方案可以在更高的数值范围精确检测手指弯曲，并检测更高自由度的手部运动。手柄产生开关式的数据，所以它们的可靠性较高；而基于视觉的解决方案虽然有较高的精度，但由于存在因视觉阻挡造成的数据跟踪丢失问题，其整体体验存在争议。
          </p>
          <p>
            拇指追踪本身也很重要。对于大多数手部追踪解决方案，其目的只是单纯的重建看起来像真手的虚拟手，而不是重建基于真手运动原理的虚拟手。例如，当你用手柄抓取虚拟物体时，虚拟的手会自动合拢呈抓握状态，然后手柄会振动。足够逼真的手部模型和动画效果可以达到障眼法的效果，在不需要将特定的反馈应用在各个手指的时候，这是可行的。但这不是真实的力反馈，甚至算不上触觉反馈。为了让力或振动发生在适合的位置并输出适合的大小，手的虚拟重建需要尽可能接近真实。我们的拇指有3个自由度，如果我们一开始就忽略拇指数据，那我们就不可能准确提供反馈。这就是为什么我们的Dexmo
            具备捕捉所有拇指动作的能力。
          </p>
          <h3>触觉和力反馈</h3>
          <p>
            触觉这个词现在被过于宽泛地使用，以至于产生了一定误导性。触觉的原意是“涉及触觉的任何交互方式”，它涵盖了很多方面：振动、纹理、温度、力传导。由于许多游戏手柄都内置振动电机，然后以“触觉反馈”的名义进行宣传，许多人便将“触觉”视为狭义的“振动”，这是种常见的误解。
          </p>
          <p>
            触觉其实有多种形式。为了帮助大家理解，我将以产品为例，主要解释不同类型的振动和力反馈的区别：
          </p>
          <ul>
            <li>
              单点振动：Xbox手柄（手柄两侧各两点），操控手柄，部分数据手套。
            </li>
            <li>多点振动：VR触觉反馈背心，其多个电机均匀分布在整个表面。</li>
            <li>
              单点式力反馈： Sensable Phantom力反馈设备，Novint Falcon
              力反馈控制器，它们在一个点提供6自由度的力反馈。
            </li>
            <li>多点式力反馈：Dexmo，为多个点提供局部力反馈。</li>
          </ul>
        </div>
