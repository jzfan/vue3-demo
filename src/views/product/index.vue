              在2014年我们首次公开亮相，我最初的想法是打造一个外骨骼，不仅可以捕捉手部运动，还可以在它的指尖提供基于微型制动盘结构的开关式力反馈功能。这个尝试成功了，它确实可行，但还不够好。最大的缺陷是高延迟和机械制动器导致的不连续力反馈。所以我们又重头开始研究。
