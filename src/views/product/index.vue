              Dexmo能够捕捉全手11个自由度的动作，包括大拇指的
              3个自由度（旋转、展开和弯曲）和其余四指各2个自由度（展开和弯曲）。微型力反馈单元可以对用户的指尖施加可达10N的力。其优秀的拇指跟踪，有助于精确重建手部模型。用户可以感受虚拟物体的大小、形状和刚度，从而大大提升了沉浸感，并获得更直观的交互。
