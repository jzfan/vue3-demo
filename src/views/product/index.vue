              不可避免地，由于各种各样的随机问题，其中一些环节可能会以令人难以置信方式重新反复数次，使得这一切更加更难以推进。例如，在上次的批量生产流程里，当我们以为自己已经掌握了每一道细节时，一个供应商告诉我们，我们的力反馈单元上有些细小的零件被不小心遗留在阳极氧化池里，然后溶解了。这件事导致整个组装推迟了一个月。当我听到这个消息时我放声大笑了很久。苦笑。
